import turtle
def draw_square():
    brad=turtle.Turtle()
    brad.shape("turtle")
    brad.color("red")
    window=turtle.Screen()
    window.bgcolor("yellow")
    brad.forward(100)
    brad.right(120)
    brad.forward(100)
    brad.right(120)
    brad.forward(100)
    brad.right(120)
    brad.forward(100)
    brad.right(120)
    brad.forward(100)
    brad.right(120)
    brad.forward(100)
    brad.right(120)
    brad.forward(100)
    window.exitonclick()
    

draw_square()
